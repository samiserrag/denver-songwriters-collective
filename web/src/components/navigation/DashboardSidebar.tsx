interface DashboardSidebarProps {
  isAdmin: boolean;
  isMobileOpen?: boolean;
  onMobileClose?: () => void;
}

export function DashboardSidebar({ isAdmin, isMobileOpen, onMobileClose }: DashboardSidebarProps) {
  const pathname = usePathname();

  const isActive = (href: string) => {
    if (href === "/dashboard") {
      return pathname === "/dashboard";
    }
    return pathname.startsWith(href);
  };

  const navSections: NavSection[] = [
    { items: mainNavItems },
    ...(isAdmin ? [{ title: "Admin", items: adminNavItems, adminOnly: true }] : []),
  ];

  const sidebarContent = (
    <nav className="flex flex-col h-full" aria-label="Dashboard navigation">
      <div className="flex-1 py-4 overflow-y-auto">
        {navSections.map((section, sectionIdx) => (
          <div key={sectionIdx} className={cn(sectionIdx > 0 && "mt-6")}>
            {section.title && (
              <h3 className="px-4 mb-2 text-xs font-semibold uppercase tracking-wider text-[var(--color-text-tertiary)]">
                {section.title}
              </h3>
            )}
            <ul className="space-y-1 px-2">
              {section.items.map((item) => {
                const active = isActive(item.href);
                return (
                  <li key={item.href}>
                    <Link
                      href={item.href}
                      onClick={onMobileClose}
                      className={cn(
                        "flex items-center gap-3 px-3 py-2 text-sm font-medium rounded-lg transition-colors",
                        active
                          ? "bg-[var(--color-accent-primary)] text-[var(--color-text-on-accent)]"
                          : "text-[var(--color-text-secondary)] hover:bg-[var(--color-bg-hover)] hover:text-[var(--color-text-primary)]"
                      )}
                      aria-current={active ? "page" : undefined}
                    >
                      <span className={cn(active ? "text-[var(--color-text-on-accent)]" : "text-[var(--color-text-tertiary)]")}>
                        {item.icon}
                      </span>
                      {item.label}
                    </Link>
                  </li>
                );
              })}
            </ul>
          </div>
        ))}
      </div>
    </nav>
  );

  return (
    <>
      {/* Desktop sidebar */}
      <aside className="hidden lg:flex lg:flex-col lg:w-64 lg:fixed lg:inset-y-0 lg:top-16 lg:border-r lg:border-[var(--color-border-subtle)] lg:bg-[var(--color-bg-primary)]">
        {sidebarContent}
      </aside>

      {/* Mobile sidebar overlay */}
      {isMobileOpen && (
        <div className="lg:hidden fixed inset-0 z-50">
          {/* Backdrop */}
          <div
            className="absolute inset-0 bg-black/60 backdrop-blur-sm"
            onClick={onMobileClose}
            aria-hidden="true"
          />
          {/* Sidebar panel */}
          <aside className="absolute left-0 top-0 bottom-0 w-64 bg-[var(--color-bg-primary)] border-r border-[var(--color-border-subtle)] shadow-xl">
            <div className="flex items-center justify-between h-16 px-4 border-b border-[var(--color-border-subtle)]">
              <span className="font-semibold text-[var(--color-text-primary)]">Menu</span>
              <button
                onClick={onMobileClose}
                className="p-2 text-[var(--color-text-secondary)] hover:text-[var(--color-text-primary)] rounded-lg transition-colors"
                aria-label="Close menu"
              >
                <svg xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" strokeWidth={1.5} stroke="currentColor" className="w-5 h-5">
                  <path strokeLinecap="round" strokeLinejoin="round" d="M6 18 18 6M6 6l12 12" />
                </svg>
              </button>
            </div>
            {sidebarContent}
          </aside>
        </div>
      )}
    </>
  );
}

